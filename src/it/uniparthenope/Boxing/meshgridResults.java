package it.uniparthenope.Boxing;

public class meshgridResults {
    Double[][] X;
    Double[][] Y;

    public meshgridResults(Double[][] X, Double[][] Y){
        this.X = X;
        this.Y = Y;
    }

    public Double[][] getX() {
        return X;
    }

    public Double[][] getY() {
        return Y;
    }
}
